package xyz.egoistk21.iFantasy.bean;

public class RecruitInfo {

    private int num;
    private int time;

    public int getNum() {
        return num;
    }

    public int getTime() {
        return time;
    }

    @Override
    public String toString() {
        return "RecruitInfo{" +
                "num=" + num +
                ", time=" + time +
                '}';
    }
}
